window.currentLang = localStorage.getItem('lang') || 'zh';

function switchLang() {
  var btn = document.getElementById('langToggle');
  if (!btn) return;
  window.currentLang = window.currentLang === 'zh' ? 'en' : 'zh';
  btn.textContent = window.currentLang === 'zh' ? '中' : 'EN';
  localStorage.setItem('lang', window.currentLang);

  var walker = document.createTreeWalker(document.body, 4, null, false);
  var nodes = [], node;
  while (node = walker.nextNode()) { nodes.push(node); }

  for (var i = 0; i < nodes.length; i++) {
    var n = nodes[i];
    if (!n.parentNode || n.parentNode.tagName === 'SCRIPT' || n.parentNode.tagName === 'STYLE') continue;
    var text = n.textContent.replace(/\s+/g, ' ').trim();
    if (!text || text.length < 2) continue;

    if (window.currentLang === 'en') {
      if (window.i18nDict[text]) {
        n._origZh = text;
        n.textContent = window.i18nDict[text];
      } else {
        var clean = text.replace(/[✓✅★▶▸]/g, '').trim();
        if (clean !== text && window.i18nDict[clean]) {
          n._origZh = text;
          n.textContent = window.i18nDict[clean];
        }
      }
    } else if (n._origZh) {
      n.textContent = n._origZh;
      delete n._origZh;
    }
  }
}

if (window.currentLang === 'en') { setTimeout(switchLang, 200); }
